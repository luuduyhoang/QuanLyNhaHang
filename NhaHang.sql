 SET @IDNL =  DBO.NEW_ID_NL();
 INSERT INTO NGUYEN_LIEU(ID_NL, TEN_NL, SO_LUONG) VALUES (@IDNL, @TENNL, 0)
END;
ELSE BEGIN
	SELECT @IDNL = ID_NL FROM NGUYEN_LIEU WHERE TEN_NL = @TENNL
END;
