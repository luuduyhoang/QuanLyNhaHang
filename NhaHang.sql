CREATE PROC [dbo].[ADDDETAILINPUT]
	@TENNL varchar(20),
	@SOLUONG INT,
	@DONGIA FLOAT,
	@IDNHAP CHAR(10)
AS
BEGIN
	DECLARE @IDNL CHAR(10)
