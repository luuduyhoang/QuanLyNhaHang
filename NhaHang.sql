BEGIN
	insert into PHIEU_NHAP (ID_NHAP) VALUES (@IDNHAP)
	insert into NHAP_NL(ID_NL,ID_NHAP, SOLUONG, DON_GIA) 
values (@IDNL, @IDNHAP, @SOLUONG, @DONGIA)
END;
ELSE BEGIN
insert into NHAP_NL(ID_NL,ID_NHAP, SOLUONG, DON_GIA) 
values (@IDNL, @IDNHAP, @SOLUONG, @DONGIA)
END;
END
